public class ejercicio5_05 {
 
    public static void main(String[] args) {
      
      int i = 320;
     
      System.out.println("Este programa muestra los números del 320 al 160 de 20 en 20 hacia atrás:");
      
      
      while (i >= 160) {
        System.out.println(i);
        i-=20;
        
       } 
        
  }
}  
